class CircleArea{
	public static void main(String[] args){
		double pi = 3.14;
		double r1 = 1.2,r2=2.5,r3=6.0;
		double area1,area2,area3;
		area1 = pi * r1 * r1;
		area2 = pi * r2 * r2;
		area3 = pi * r3 * r3;
		System.out.println("圆1的面积为"+area1);
		System.out.println("圆2的面积为"+area2);
		System.out.println("圆3的面积为"+area3);
	}
}